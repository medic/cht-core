import { createAction } from '@ngrx/store';

export const createSingleValueAction = (type, valueName:string) => {
  return createAction(
    type,
    (value) => ({ payload: {[valueName]: value} }),
  );
};

export const createMultiValueAction = (type) => {
  return createAction(
    type,
    (payload) => ({ payload: payload }),
  );
};

